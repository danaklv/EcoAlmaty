package seeders

import (
	"database/sql"
	"fmt"
)

func SeedEcoQuestions(db *sql.DB) error {
	var count int
	err := db.QueryRow(`SELECT COUNT(*) FROM eco_questions`).Scan(&count)
	if err != nil {
		return err
	}

	if count > 0 {
		fmt.Println("eco_questions already seeded")
		return nil
	}

	fmt.Println("Seeding eco_questions...")

	_, err = db.Exec(`
		INSERT INTO eco_questions 
		(category, question, max_value, weight, is_reverse, tip) VALUES

		-- WATER
		('water', 'Как часто вы закрываете кран, когда вода не используется?', 5, 1.2, true,
		 'Закрывайте кран во время чистки зубов, мытья посуды или намыливания рук.'),

		('water', 'Как часто вы принимаете короткий душ вместо длительного?', 5, 1.1, true,
		 'Попробуйте сократить время душа хотя бы на 2–3 минуты.'),

		('water', 'Как часто вы запускаете стиральную машину только при полной загрузке?', 5, 1.0, true,
		 'Старайтесь запускать стирку только при полной загрузке.'),

		-- ENERGY
		('energy', 'Как часто вы выключаете свет, выходя из комнаты?', 5, 1.2, true,
		 'Выключайте свет в комнатах, где никого нет.'),

		('energy', 'Как часто вы отключаете зарядки и технику, когда они не используются?', 5, 1.0, true,
		 'Отключайте зарядки и технику от розетки, если они не нужны.'),

		('energy', 'Как часто вы используете кондиционер или обогреватель без необходимости?', 5, 1.2, false,
		 'Используйте кондиционер и обогреватель умеренно.'),

		-- TRANSPORT
		('transport', 'Как часто вы выбираете пешую прогулку для коротких расстояний?', 5, 1.2, true,
		 'Для коротких расстояний чаще выбирайте ходьбу.'),

		('transport', 'Как часто вы пользуетесь общественным транспортом вместо такси или личного авто?', 5, 1.3, true,
		 'Общественный транспорт помогает снизить личный углеродный след.'),

		('transport', 'Как часто вы пользуетесь такси или личным автомобилем без реальной необходимости?', 5, 1.3, false,
		 'Попробуйте заменить часть поездок пешей прогулкой или общественным транспортом.'),

		-- FOOD
		('food', 'Как часто вы планируете покупки, чтобы не выбрасывать продукты?', 5, 1.2, true,
		 'Планирование покупок помогает сократить пищевые отходы.'),

		('food', 'Как часто вы используете многоразовую бутылку, стакан или контейнер?', 5, 1.1, true,
		 'Берите с собой многоразовую бутылку или контейнер.'),

		('food', 'Как часто вы заказываете еду с большим количеством одноразовой упаковки?', 5, 1.0, false,
		 'По возможности выбирайте доставку без лишней упаковки.'),

		-- WASTE
		('waste', 'Как часто вы сортируете отходы для переработки?', 5, 1.3, true,
		 'Начните хотя бы с пластика, бумаги и стекла.'),

		('waste', 'Как часто вы используете многоразовую сумку при покупках?', 5, 1.1, true,
		 'Держите многоразовую сумку в рюкзаке, сумке или машине.'),

		('waste', 'Как часто вы используете одноразовые пакеты, стаканы или посуду?', 5, 1.2, false,
		 'Старайтесь заменять одноразовые вещи многоразовыми.')
	`)

	if err != nil {
		return err
	}

	fmt.Println("✔ eco_questions seeded successfully")
	return nil
}